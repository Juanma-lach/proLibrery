package net.daw.bean.generic.specific.implementation;

import com.google.gson.annotations.Expose;
import net.daw.bean.generic.implementation.BeanGenImpl;
import net.daw.bean.publicinterface.BeanInterface;

public class LibroBeanGenSpImpl extends BeanGenImpl implements BeanInterface {
    @Expose
    private String titulo = "";
    @Expose
    private String isbn = "";
    @Expose
    private String editorial = "";
    @Expose
    private String urlimagen = "";
    @Expose
    private Double puntuacion = 0.0;

    public LibroBeanGenSpImpl() {

    }

    public LibroBeanGenSpImpl(Integer id) {
        super(id);
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }

    public String getEditorial() {
        return editorial;
    }

    public void setEditorial(String editorial) {
        this.editorial = editorial;
    }

    public String getUrlimagen() {
        return urlimagen;
    }

    public void setUrlimagen(String url_imagen) {
        this.urlimagen = url_imagen;
    }

    public Double getPuntuacion() {
        return puntuacion;
    }

    public void setPuntuacion(Double puntuacion) {
        this.puntuacion = puntuacion;
    }

    
    
}
